package com.seeka.app.dto;

import java.math.BigInteger;

public class HelpDto {

    private String title;
    private BigInteger categoryId;
    private BigInteger subCategoryId;
    private String description;
    private String createdBy;
    private String updatedBy;
    private Boolean questioning;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public BigInteger getCategoryId() {
        return categoryId;
    }

    public void setCategoryId(BigInteger categoryId) {
        this.categoryId = categoryId;
    }

    public BigInteger getSubCategoryId() {
        return subCategoryId;
    }

    public void setSubCategoryId(BigInteger subCategoryId) {
        this.subCategoryId = subCategoryId;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
    

    public String getCreatedBy() {
        return createdBy;
    }

    public void setCreatedBy(String createdBy) {
        this.createdBy = createdBy;
    }

    public String getUpdatedBy() {
        return updatedBy;
    }

    public void setUpdatedBy(String updatedBy) {
        this.updatedBy = updatedBy;
    }

    public Boolean getQuestioning() {
        return questioning;
    }

    public void setQuestioning(Boolean questioning) {
        this.questioning = questioning;
    }

    
}
